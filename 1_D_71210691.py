hujan = int(input("Masukkan nilai curah hujan : "))
if hujan >= 100:
    print("Curah hujan ekstrem")
elif hujan >=  51:
    print("Curah hujan lebat")
elif hujan >= 21:
    print("Curah hujan sedang")
elif hujan >= 0.5 :
    print("Curah hujan ringan")
elif hujan >= 0:
    print("Cuaca terang atau berawan")
